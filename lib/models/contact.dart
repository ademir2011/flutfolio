class Contact {
  final String name;
  final DateTime birthDate;
  final String state;
  final String city;
  final String number;
  final String githubUrl;
  final String linkedinUrl;

  Contact({
    required this.name,
    required this.birthDate,
    required this.state,
    required this.city,
    required this.number,
    required this.githubUrl,
    required this.linkedinUrl,
  });
}
